let heftegunu = +prompt("Sənə hansı gün lazımdı brat?")

switch(heftegunu) 
{
    case 1: document.write ("Bu gün Bazar ertəsidir");
    break
    case 2: document.write ("Bu gün Çərşənbə axşamıdır");
    break
    case 3: document.write ("Bu gün Çərşənbədir");
    break
    case 4: document.write ("Bu gün Cümə axşamıdır");
    break
    case 5: document.write ("Bu gün Cümədir");
    break
    case 6: document.write ("Bu gün Şənbədir");
    break
    case 7: document.write ("Bu gün Bazardır");
    break

    default: document.write ("Elə gün var ala?!")
    
}