let dir = prompt("istiqamət seç...")
while(true)
{
    if(dir == "up")
    {
    dir = prompt("yuxarı get")
    }
    else if(dir == "down")
    {
    dir = prompt("aşağı get")
    }
    else if(dir == "left")
    {
    dir = prompt("sola get")
    }
    else if(dir == "right")
    {
    dir = prompt("sağa get")
    }
    else
    {
        dir = prompt("Ə get istiqamətləri öyrən...")
    }
}